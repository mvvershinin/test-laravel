    public function byName(Request $request)
    {
        $match = $request['search-value'];

        $result = Doctor::where('surname', 'like', $match.'%')
                ->orWhere('name', 'like', $match.'%')
                ->orWhere('patronymic', 'like', $match.'%')
                ->paginate(15);

        return response()
            ->json($result);
            
    }

